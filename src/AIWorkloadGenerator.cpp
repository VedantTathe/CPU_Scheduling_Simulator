std::string AIWorkloadGenerator::callDeepSeekAPI(const std::string& prompt, const std::string& apiKey) {
    // 1. Clean the prompt of unsafe shell characters
    std::string safePrompt = prompt;
    safePrompt.erase(std::remove(safePrompt.begin(), safePrompt.end(), '"'), safePrompt.end());
    safePrompt.erase(std::remove(safePrompt.begin(), safePrompt.end(), '\\'), safePrompt.end());
    safePrompt.erase(std::remove(safePrompt.begin(), safePrompt.end(), '`'), safePrompt.end());
    safePrompt.erase(std::remove(safePrompt.begin(), safePrompt.end(), '\n'), safePrompt.end());

    // 2. Build the curl system command
    // We request the model to return a raw JSON list of processes with properties: pid, name, arrival, burst, priority.
    std::string url = "https://api.deepseek.com/chat/completions";
    
    std::ostringstream jsonStream;
    jsonStream << "{\\\"model\\\":\\\"deepseek-chat\\\",\\\"messages\\\":[{\\\"role\\\":\\\"user\\\",\\\"content\\\":\\\"Generate a valid raw JSON array of realistic processes for: '" 
               << safePrompt << "'. Format: [ {\\\\\\\"pid\\\\\\\":1, \\\\\\\"name\\\\\\\":\\\\\\\"GameEngine\\\\\\\", \\\\\\\"arrival\\\\\\\":0, \\\\\\\"burst\\\\\\\":8, \\\\\\\"priority\\\\\\\":1} ]. Only return the JSON array, no markdown wrappers, no backticks, no comments, no extra text.\\\"}],\\\"temperature\\\":0.2}";
    
    std::string cmd = "curl -s -X POST \"" + url + "\" ";
    cmd += "-H \"Content-Type: application/json\" ";
    cmd += "-H \"Authorization: Bearer " + apiKey + "\" ";
    cmd += "-d \"" + jsonStream.str() + "\"";

    // 3. Open pipeline
    FILE* fp = popen(cmd.c_str(), "r");
    if (!fp) {
        std::cerr << "❌ [AIWorkloadGenerator] Failed to open curl subprocess pipeline!" << std::endl;
        return "";
    }

    char buffer[1024];
    std::string result = "";
    while (fgets(buffer, sizeof(buffer), fp) != NULL) {
        result += buffer;
    }
    pclose(fp);
    return result;
}

std::string extractJSONStringField(const std::string& json, const std::string& fieldName) {
    size_t pos = json.find("\"" + fieldName + "\"");
    if (pos == std::string::npos) {
        return "";
    }
    pos = json.find(":", pos + fieldName.length() + 2);
    if (pos == std::string::npos) {
        return "";
    }
    pos = json.find("\"", pos + 1);
    if (pos == std::string::npos) {
        return "";
    }
    size_t start = pos + 1;
    std::string value = "";
    for (size_t i = start; i < json.length(); ++i) {
        if (json[i] == '\\') {
            if (i + 1 < json.length()) {
                char c = json[i + 1];
                if (c == 'n') value += '\n';
                else if (c == 't') value += '\t';
                else if (c == 'r') value += '\r';
                else value += c;
                i++;
            }
        } else if (json[i] == '"') {
            break; // End of string
        } else {
            value += json[i];
        }
    }
    return value;
}

std::vector<Process> AIWorkloadGenerator::parseJSONResponse(const std::string& jsonStr) {
    std::vector<Process> processes;

    // Extract the inner content/text field to handle double escaping properly
    std::string textContent = extractJSONStringField(jsonStr, "content");
    if (textContent.empty()) {
        textContent = extractJSONStringField(jsonStr, "text");
    }
    
    // Fallback to the whole raw string if we didn't find those fields
    if (textContent.empty()) {
        textContent = jsonStr;
    }
    
    // Now isolate the process array using brackets
    size_t startBracket = textContent.find('[');
    size_t endBracket = textContent.rfind(']');
    
    if (startBracket != std::string::npos && endBracket != std::string::npos && endBracket > startBracket) {
        textContent = textContent.substr(startBracket, endBracket - startBracket + 1);
    } else {
        return processes;
    }

    // Parse each process object in the JSON array: { "pid": 1, "name": "GameEngine", "arrival": 0, "burst": 8, "priority": 1 }
    // We use a regex to match process structures
    std::regex objRegex("\\{[^{}]*\\}");
    auto words_begin = std::sregex_iterator(textContent.begin(), textContent.end(), objRegex);
    auto words_end = std::sregex_iterator();

    for (std::sregex_iterator it = words_begin; it != words_end; ++it) {
        std::string objStr = it->str();

        // Extract properties using regexes
        std::regex pidReg("\"pid\"\\s*:\\s*([0-9]+)");
        std::regex nameReg("\"name\"\\s*:\\s*\"([^\"]+)\"");
        std::regex arrivalReg("\"(arrival|arrivalTime)\"\\s*:\\s*([0-9]+)");
        std::regex burstReg("\"(burst|burstTime)\"\\s*:\\s*([0-9]+)");
        std::regex prioReg("\"(priority)\"\\s*:\\s*([0-9]+)");

        std::smatch match;
        int pid = 0;
        std::string name = "";
        int arrival = 0;
        int burst = 0;
        int priority = 0;

        if (std::regex_search(objStr, match, pidReg) && match.size() > 1) pid = std::stoi(match[1].str());
        if (std::regex_search(objStr, match, nameReg) && match.size() > 1) name = match[1].str();
        if (std::regex_search(objStr, match, arrivalReg) && match.size() > 2) arrival = std::stoi(match[2].str());
        if (std::regex_search(objStr, match, burstReg) && match.size() > 2) burst = std::stoi(match[2].str());
        if (std::regex_search(objStr, match, prioReg) && match.size() > 2) priority = std::stoi(match[2].str());

        if (pid > 0 && burst > 0) {
            processes.emplace_back(pid, arrival, burst, priority);
            if (!name.empty()) {
                processes.back().name = name;
            }
        }
    }

    return processes;
}
