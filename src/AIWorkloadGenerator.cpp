std::vector<Process> AIWorkloadGenerator::generateWorkload(const std::string& prompt) {
    std::string cleanPrompt = prompt;
    // Strip surrounding spaces
    cleanPrompt.erase(0, cleanPrompt.find_first_not_of(" \t\r\n"));
    cleanPrompt.erase(cleanPrompt.find_last_not_of(" \t\r\n") + 1);

    if (cleanPrompt.empty()) {
        std::cout << AIC::RED << "⚠️ Prompt cannot be empty. Generating default workload." << AIC::RESET << std::endl;
        return generateOfflineWorkload("default");
    }

    // Check if DEEPSEEK_API_KEY is available
    const char* apiKeyEnv = std::getenv("DEEPSEEK_API_KEY");
    if (apiKeyEnv && std::string(apiKeyEnv).length() > 5) {
        std::string apiKey = std::string(apiKeyEnv);
        std::cout << AIC::CYAN << "[AIWorkloadGenerator] DEEPSEEK_API_KEY detected! Querying DeepSeek AI..." << AIC::RESET << std::endl;
        
        std::string apiResponse = callDeepSeekAPI(cleanPrompt, apiKey);
        if (!apiResponse.empty()) {
            std::vector<Process> apiProcList = parseJSONResponse(apiResponse);
            if (!apiProcList.empty()) {
                std::cout << AIC::GREEN << "🚀 [AIWorkloadGenerator] Successfully loaded " << apiProcList.size() 
                          << " processes dynamically generated by DeepSeek AI!" << AIC::RESET << std::endl;
                return apiProcList;
            }
        }
        std::cout << AIC::YELLOW << "⚠️ [AIWorkloadGenerator] DeepSeek API query failed or returned invalid JSON. Falling back to local offline smart engine..." << AIC::RESET << std::endl;
    } else {
        std::cout << AIC::CYAN << "[AIWorkloadGenerator] DEEPSEEK_API_KEY env not configured. Using local offline smart generation engine..." << AIC::RESET << std::endl;
    }

    return generateOfflineWorkload(cleanPrompt);
}

